/// Enum for different mathamatical operation types
///
/// ARITHMATIC represents different arithmatic operators i.e Addition, Subtraction, etc.
///
/// TRIGONOMETRIC represents different trigonometric operations such as Sin, Cos, Tan, etc.
/// 
/// STATISTICAL represents different statistical operations such as mean, mod, median, etc.
pub enum OperationType {
    ARITHMATIC,
    TRIGONOMETRIC,
    STATISTICAL
}